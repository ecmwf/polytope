/// Compute Gauss-quadrature latitude values for a Reduced Gaussian grid of the given resolution.
/// For N320 returns the precomputed table; for other resolutions uses Newton-Raphson iteration.
///
/// Returns latitudes sorted from largest (most-south negative) to smallest (most-north positive)
/// i.e. descending order (south-pole first), matching the Python ordering.
#[pyfunction]
pub fn first_axis_vals_reduced_gaussian(resolution: usize) -> PyResult<Vec<f64>> {
    if resolution == 320 {
        let mut lats = N320_LATS.to_vec();
        lats.reverse(); // N320_LATS is south→north; Python expects north→south
        return Ok(lats);
    }

    let precision = 1.0e-14_f64;
    let nval = resolution * 2;
    let rad2deg = 180.0 / std::f64::consts::PI;
    let convval = 1.0 - ((2.0 / std::f64::consts::PI) * (2.0 / std::f64::consts::PI)) * 0.25;
    let first_guess = gauss_first_guess(resolution);
    let mut new_vals = vec![0.0_f64; nval];
    let denom = ((nval as f64 + 0.5) * (nval as f64 + 0.5) + convval).sqrt();

    for jval in 0..resolution {
        let mut root = (first_guess[jval] / denom).cos();
        let mut conv = 1.0_f64;
        while conv.abs() >= precision {
            let mut mem2 = 1.0_f64;
            let mut mem1 = root;
            for legi in 0..nval {
                let legfonc = ((2.0 * (legi as f64 + 1.0) - 1.0) * root * mem1
                    - legi as f64 * mem2)
                    / (legi as f64 + 1.0);
                mem2 = mem1;
                mem1 = legfonc;
            }
            conv = mem1 / ((nval as f64 * (mem2 - root * mem1)) / (1.0 - root * root));
            root -= conv;
        }
        new_vals[jval] = root.asin() * rad2deg;
        new_vals[nval - 1 - jval] = -new_vals[jval];
    }

    // Python orders from most-positive (north) to most-negative (south) — the loop above
    // fills indices 0..resolution with northern hemisphere values (positive) and mirrors them.
    // new_vals[0] is already the most-northern value; no reversal needed.
    Ok(new_vals)
}

// ---------------------------------------------------------------------------
// Build the cumulative first-index map (matches octahedral's create_first_idx_list pattern)
// ---------------------------------------------------------------------------

fn create_reduced_gaussian_first_idx_map(lon_spacing: &[usize]) -> Vec<usize> {
    let mut map = Vec::with_capacity(lon_spacing.len());
    let mut cumsum = 0usize;
    for &n in lon_spacing {
        map.push(cumsum);
        cumsum += n;
    }
    map
}

