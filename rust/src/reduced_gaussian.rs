/// Map `(first_val, second_vals)` to flat grid indices for a Reduced Gaussian grid.
///
/// This is the Rust equivalent of Python's `ReducedGaussianGridMapper.unmap()`.
/// It replaces the O(N) linear scan with binary search for both the latitude and
/// longitude lookups.
///
/// `resolution`  : grid resolution (e.g. 320)
/// `first_val`   : a one-element list containing the target latitude
/// `second_vals` : list of target longitudes
///
/// Returns a list of flat grid indices.
#[pyfunction]
pub fn unmap_reduced_gaussian(
    resolution: usize,
    first_val: Vec<f64>,
    second_vals: Vec<f64>,
) -> PyResult<Vec<usize>> {
    let first_axis_vals = first_axis_vals_reduced_gaussian(resolution)?;
    let lon_spacing = if resolution == 320 {
        N320_LON_SPACING.to_vec()
    } else {
        return Err(PyValueError::new_err(format!(
            "unmap_reduced_gaussian: lon_spacing not yet implemented for resolution {}",
            resolution
        )));
    };

    // Use 1e-6 tolerance to handle the ~1.57e-7 max precision difference between
    // Python's precomputed N320 lat table and Rust's table.
    let tol = 1e-6_f64;
    let lat = first_val
        .get(0)
        .copied()
        .ok_or_else(|| PyValueError::new_err("first_val must be a non-empty list"))?;

    // Linear scan for the latitude index.
    // first_axis_vals is sorted descending (north→south), matching Python ordering.
    let first_idx = first_axis_vals
        .iter()
        .position(|&v| (v - lat).abs() < tol)
        .ok_or_else(|| {
            PyValueError::new_err(format!(
                "unmap_reduced_gaussian: latitude {} not found in first_axis_vals",
                lat
            ))
        })?;

    let ny = lon_spacing[first_idx];
    let second_spacing = 360.0 / ny as f64;

    // Build cumulative index map
    let first_idx_map = create_reduced_gaussian_first_idx_map(&lon_spacing);

    let mut return_idxs = Vec::with_capacity(second_vals.len());
    for &lon in &second_vals {
        // Find longitude index with tolerance using binary search
        let div = lon / second_spacing;
        let div_floor = div.floor() as usize;
        let tol_idx = 1e-8_f64;
        let second_idx = if div > (div_floor as f64 + 1.0 - tol_idx) {
            div_floor + 1
        } else {
            div_floor
        };
        let second_idx = second_idx % ny; // wrap around
        let flat_idx = first_idx_map[first_idx] + second_idx;
        return_idxs.push(flat_idx);
    }

    Ok(return_idxs)
}

